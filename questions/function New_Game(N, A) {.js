function New_Game(N, A) {
    var mv = 0,
        i = 0,
        c = 0,
        canMove = false;
    //sort A from large to small first
    A.sort(function(a,b){
        return b-a;
    })
    var i = 0;
    // check if there is any move available
    while (!canMove && i < N - 1) {
        var diff = A[i] - A[N-1];
        if (A.indexOf(diff) < 0) {
           canMove = true;
           break;
        }
        i++;
    }
    if (!canMove) {
        return 'HARI';
    }
    for (var m = 0; m<N; m++) {
        if (A[m]%2 === 1){
            c = 1;
            break;
        }
    }
    return (A[0]-N)%2 == c ? 'RAM':'HARI';
}

